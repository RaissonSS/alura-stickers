public class Conteudo {
    
    private final String titulo;
    private final String ulrImagem;

    public Conteudo(String titulo, String ulrImagem) {
        this.titulo = titulo;
        this.ulrImagem = ulrImagem;
    }

    public String getTitulo() {
        return titulo;
    }

    public String getUlrImagem() {
        return ulrImagem;
    }

    
}
